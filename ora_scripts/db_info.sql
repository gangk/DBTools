SELECT DBID,NAME,LOG_MODE,VERSION_TIME,OPEN_MODE,DATABASE_ROLE,SWITCHOVER_STATUS,FORCE_LOGGING,protection_mode,protection_level
FROM   v$database;

SELECT instance_name,instance_number,host_name,version,startup_time,status,database_status
FROM   v$instance;

SELECT a.name,
       a.value
FROM   v$sga a;

SELECT Substr(c.name,1,60) "Controlfile",
       NVL(c.status,'UNKNOWN') "Status"
FROM   v$controlfile c
ORDER BY 1;

SELECT Substr(d.name,1,60) "Datafile",
       NVL(d.status,'UNKNOWN') "Status",
       d.enabled "Enabled",
       LPad(To_Char(Round(d.bytes/1024000,2),'9999990.00'),10,' ') "Size (M)"
FROM   v$datafile d
ORDER BY 1;

SELECT l.group# "Group",
       Substr(l.member,1,60) "Logfile",
       NVL(l.status,'UNKNOWN') "Status"
FROM   v$logfile l
ORDER BY 1,2;

